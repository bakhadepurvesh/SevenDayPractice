package Encapsulation;

public class PracticeEncapsulation {

	private int id;
	
	private String name;
	
	private String city;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}
	
}


// Encapsulation is used to the handing the data or banding the data into single unit 
// we can declare the Private fields or access them to the setter or getter
// Encapsulation is used to validation 

